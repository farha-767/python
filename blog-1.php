                            <div class="article">
                                <h3>CONTENT MARKETING</h3>
                                <p>Content marketing serves as a powerful tool for healthcare marketers to establish
                                    trust. By creating informative and engaging content, organisations can position
                                    themselves as reliable sources of healthcare knowledge. Blogs, articles, and videos
                                    sharing health tips, and insight into latest medical breakthroughs can help build
                                    trust with the audience as it empowers them with knowledge that is both relevant and
                                    easily accessible. </p>
                            </div>
                        </div>
                        <div class="col-md-8 offset-md-2 mt-40">
                            <div class="article">
                                <h3>PERSONALISED COMMUNICATION & ENGAGEMENT</h3>
                                <p>Tailoring messages and providing relevant information based on patients’ preferences
                                    and needs not only enhance trust but also improves their engagement with the company
                                    and their overall satisfaction. Hence, it is important for healthcare organizations
                                    to leverage social media, email marketing and patient portals to establish direct
                                    and personalised communication. </p>
                            </div>
                        </div>

