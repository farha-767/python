                                <h3>INFLUENCER MARKETING</h3>
                                <p>Partnering with healthcare professionals or patient advocates who align with the
                                    organisation's values and mission can help amplify the message and reach a wider
                                    audience. The credibility and expertise of these influencers can transfer onto the
                                    organisation, enhancing trust among their followers. Further, any well-known
                                    personality endorsing the clinic or sharing their experience of your services can
                                    also help increase visibility. </p>
                            </div>
                        </div>
