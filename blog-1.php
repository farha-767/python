                            <div class="article">
                                <h3>TRANSPARENCY AND TESTIMONIALS</h3>
                                <p>Healthcare organisations should be transparent about their services, pricing, and
                                    quality of care. Displaying patient testimonials, success stories, and reviews on
                                    the website and social media platforms can provide potential patients with a glimpse
                                    into the positive experiences others have had with the organisation. Genuine and
                                    heartfelt testimonials contribute to building trust and fostering a sense of
                                    reliability. </p>
                            </div>
                        </div>
                        <div class="col-md-8 offset-md-2 mt-40">
                            <div class="article">
                                <h3>SOCIAL MEDIA ENGAGEMENTS</h3>
                                <p>Everyone’s on social media. So by putting effort into building an online community
                                    that shares relevant and reliable information can be both lucrative and helpful in
                                    creating loyal followers on social media. Additionally, proactive responses to
                                    queries helps maintain a positive reputation for your company. </p>
                            </div>
