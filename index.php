        gsap.registerPlugin(ScrollTrigger);

        // Function to check if the window width is greater than 768px
        function isWindowWidthGreaterThan768() {
        return window.innerWidth > 768;
        }

        function setupParallaxAnimation(container) {
        const img = container.querySelector('.parallax-img');

        // Check if the window width is greater than 768px before setting up the animation
        if (isWindowWidthGreaterThan768()) {
            gsap.to(img, {
            yPercent: -25, // Adjust the parallax effect here
            ease: 'none',
            scrollTrigger: {
                trigger: container,
                start: 'top 80%',
                end: 'bottom 20%',
                scrub: true
            }
            });
        }
        }

        const parallaxContainers = gsap.utils.toArray('.parallax-container');

        parallaxContainers.forEach((container) => {
        setupParallaxAnimation(container);
        });

        // Listen for window resize events and reapply the animation accordingly
        window.addEventListener('resize', () => {
        parallaxContainers.forEach((container) => {
            gsap.killTweensOf(container.querySelector('.parallax-img')); // Kill previous animation
            setupParallaxAnimation(container);
        });
        });







        ScrollTrigger.create({
            trigger: '.folio-content',
            start: 'top 80%',
            end: 'bottom 70%',
            onUpdate: (self) => {
                if (self.isActive) {
                    gsap.to(document.body, { backgroundColor: '#000', duration: 0.3 }); // Smooth transition to #000
                    gsap.to('.folio-content .main-title h2', { filter: 'invert(1)', duration: 0.2 });
                    gsap.to('.logo img', { filter: 'invert(1)', duration: 0.2 });
                    gsap.to('.one', { background: '#fff', duration: 0.3 });
                    gsap.to('.two', { background: '#fff', duration: 0.3 });
                    gsap.to('.tre', { background: '#fff', duration: 0.3 });
                    gsap.to('.next-project', { filter: 'invert(1)', duration: 0.2 }); // Invert the color

                } else {
                    gsap.to(document.body, { backgroundColor: '#fff', duration: 0.3 });
                    gsap.to('.folio-content .main-title h2', { filter: 'invert(0)', duration: 0.2 });
                    gsap.to('.logo img', { filter: 'invert(0)', duration: 0.2 });
                    gsap.to('.one', { background: '#000', duration: 0.3 });
                    gsap.to('.two', { background: '#000', duration: 0.3 });
                    gsap.to('.tre', { background: '#000', duration: 0.3 });
                    gsap.to('.next-project', { filter: 'invert(0)', duration: 0.2 });

                }
            }
        });





        function animateHeadlines() {
            const headlines = document.querySelectorAll('.sub-head-text');
            headlines.forEach(hl => {
                let newHtml = '';
                for (let i = 0; i < hl.innerHTML.length; i += 1) {
                    let ts = hl.innerHTML[i];
                    if (ts === '<') {
                        const tmpTxt = hl.innerHTML.substring(i);
                        ts = tmpTxt.match(/<.*?>/)[0];
                        i += ts.length - 1;
                    }
                    else if (ts === ' ') ts = ' ';
                    else ts = `<span style="opacity: 0; display: inline-block;">${ts}</span>`;
                    newHtml += ts;
                }
                hl.innerHTML = newHtml;
                hl.ltrs = hl.querySelectorAll('span');
                const delSpans = () => hl.innerHTML = hl.innerHTML.replace(/<span.*?>/g, '');
                gsap.timeline({ scrollTrigger: { trigger: hl, start: 'top 80%' }, onComplete: delSpans })
                    .set(hl.ltrs, { opacity: 0, x: 40, skewX: -40 })
                    .to(hl.ltrs, 0.6, { opacity: 1, x: 0, ease: 'back.out', skewX: 0, stagger: 0.02 })
            });
        }

        window.onload = animateHeadlines;








        new WOW().init();

    </script>
